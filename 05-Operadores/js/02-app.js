const num1 = 20;
const num2 = '20';
const num3 = 30;

// Revisar si dos numeros son iguales

console.log(num1 == num2);
console.log(num1 == num3);

// Comparador estricto

console.log(num1 === num2);
console.log(num1 === parseInt(num2));
console.log(typeof num1);
console.log(typeof num2);

// Diferentes

const pass1 = "admin"
const pass2 = "Admin"

console.log(pass1 != pass2);
console.log(num1 !== parseInt(num2));